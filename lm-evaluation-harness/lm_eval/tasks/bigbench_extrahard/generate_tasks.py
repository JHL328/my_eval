import os

import datasets
import yaml



all_subtasks = [
    "boardgame_qa",
    "boolean_expressions",
    "buggy_tables",
    "causal_understanding",
    "disambiguation_qa",
    "dyck_languages",
    "geometric_shapes",
    "hyperbaton",
    "linguini",
    "movie_recommendation",
    "multistep_arithmetic",
    "nycc",
    "object_counting",
    "object_properties",
    "sarc_triples",
    "shuffled_objects",
    "spatial_reasoning",
    "sportqa",
    "temporal_sequence",
    "time_arithmetic",
    "web_of_lies",
    "word_sorting",
    "zebra_puzzles"
]

def main() -> None:
    os.makedirs("data", exist_ok=True)
    for task in all_subtasks:
        file_name = f"{task}.yaml"
        try:
            template_file = "bigbench_extrahard_template.yaml"
            path = "data" 
            with open(f"{path}/{file_name}", "w", encoding="utf-8") as f:
                f.write("# Generated by utils.py\n")
                yaml.dump(
                    {
                        "include": f"../{template_file}",
                        "task": "bigbench_extrahard_"
                        + task,
                        "dataset_name": task
                    },
                    f,
                    width=float("inf"),
                    allow_unicode=True,
                )
        except FileExistsError:
            pass


if __name__ == "__main__":
    main()
